# Confirm Reboot
# ------------------------------------------------------

while true; do
    read -p "Do you really want to Reboot? (y/n): " yn
    case $yn in
        [Yy]* )
            systemctl reboot
        break;;
        [Nn]* )
            exit;
        break;;
        * ) echo "Please answer yes or no.";;
    esac
done

