# Confirm Shutdown
# ------------------------------------------------------

while true; do
    read -p "Do you really want to Shutdown? (y/n): " yn
    case $yn in
        [Yy]* )
            systemctl poweroff
        break;;
        [Nn]* )
            exit;
        break;;
        * ) echo "Please answer yes or no.";;
    esac
done

